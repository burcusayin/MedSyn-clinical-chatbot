def normalize_text(s: str) -> str:
    if s is None or (isinstance(s, float) and np.isnan(s)):
        return ""
    s = str(s).strip().lower()
    s = unicodedata.normalize("NFKD", s)
    s = s.replace("&", " and ")
    s = re.sub(r"[\(\)\[\]\{\}]", " ", s)
    s = re.sub(r"[^a-z0-9\s]", " ", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s


def extract_primary_segment(text: str) -> str:
    """
    If the discharge diagnosis contains 'primary:'/'secondary:' markers, keep only the primary segment.
    Otherwise, return the whole string.
    """
    if text is None or (isinstance(text, float) and np.isnan(text)):
        return ""
    t = str(text)
    low = t.lower()
    if "primary:" in low:
        m = re.search(r"primary\s*:?", low)
        if m:
            seg = t[m.end():]
            m2 = re.search(r"secondary\s*:?", seg.lower())
            if m2:
                seg = seg[:m2.start()]
            return seg.strip()
    return t.strip()


def split_diagnoses(text: str, *, is_gold: bool) -> List[str]:
    """
    Gold: primarily newline-separated; tolerate semicolons too (observed in some datasets).
    Answers: primarily semicolon-separated; tolerate newlines too.
    """
    seg = extract_primary_segment(text)
    if not seg:
        return []
    seg = seg.replace("\r\n", "\n").replace("\r", "\n").strip()

    parts: List[str] = []
    if is_gold:
        for line in seg.split("\n"):
            line = line.strip()
            if not line:
                continue
            for p in re.split(r"\s*;\s*", line):
                p = p.strip()
                if p:
                    parts.append(p)
        return parts

    for chunk in seg.split("\n"):
        chunk = chunk.strip()
        if not chunk:
            continue
        for p in re.split(r"\s*;\s*", chunk):
            p = p.strip()
            if p:
                parts.append(p)
    return parts


def fuzzy_match_count(pred_list: List[str], gold_list: List[str], threshold: int) -> Tuple[int, List[Tuple[int,int,float]]]:
    """
    Greedy one-to-one matching using token_set_ratio.
    Returns number of matches and list of (i_pred, j_gold, similarity).
    """
    pred_norm = [normalize_text(x) for x in pred_list if normalize_text(x)]
    gold_norm = [normalize_text(x) for x in gold_list if normalize_text(x)]
    if not pred_norm or not gold_norm:
        return 0, []

    pairs: List[Tuple[float,int,int]] = []
    for i, p in enumerate(pred_norm):
        for j, g in enumerate(gold_norm):
            sim = float(fuzz.token_set_ratio(p, g))
            if sim >= threshold:
                pairs.append((sim, i, j))

    pairs.sort(reverse=True)
    used_i, used_j = set(), set()
    matches: List[Tuple[int,int,float]] = []
    for sim, i, j in pairs:
        if i in used_i or j in used_j:
            continue
        used_i.add(i)
        used_j.add(j)
        matches.append((i, j, sim))
    return len(matches), matches


def compute_metrics(pred_list: List[str], gold_list: List[str], threshold: int) -> Dict[str, float]:
    m, _ = fuzzy_match_count(pred_list, gold_list, threshold=threshold)

    pred_norm = [normalize_text(x) for x in pred_list if normalize_text(x)]
    gold_norm = [normalize_text(x) for x in gold_list if normalize_text(x)]

    precision = m / len(pred_norm) if pred_norm else 0.0
    recall = m / len(gold_norm) if gold_norm else 0.0
    f1 = 0.0 if (precision + recall) == 0 else 2 * precision * recall / (precision + recall)
    any_match = 1.0 if m >= 1 else 0.0
    exact = 1.0 if (len(pred_norm) == len(gold_norm) == m and (m > 0 or (len(pred_norm) == 0 and len(gold_norm) == 0))) else 0.0

    return dict(matches=float(m), precision=precision, recall=recall, f1=f1, any_match=any_match, exact_fuzzy=exact)


def bootstrap_ci_from_values(vals: np.ndarray, n_boot: int = 5000, seed: int = 42) -> Tuple[float,float]:
    rng = np.random.default_rng(seed)
    vals = np.asarray(vals, dtype=float)
    n = len(vals)
    boots = rng.choice(vals, size=(n_boot, n), replace=True).mean(axis=1)
    return float(np.quantile(boots, 0.025)), float(np.quantile(boots, 0.975))


def bootstrap_ci_diff(valsA: np.ndarray, valsB: np.ndarray, n_boot: int = 5000, seed: int = 42) -> Tuple[float,float]:
    rng = np.random.default_rng(seed)
    valsA = np.asarray(valsA, dtype=float)
    valsB = np.asarray(valsB, dtype=float)
    n = len(valsA)
    idx = np.arange(n)
    boots_idx = rng.choice(idx, size=(n_boot, n), replace=True)
    diffs = valsB[boots_idx].mean(axis=1) - valsA[boots_idx].mean(axis=1)
    return float(np.quantile(diffs, 0.025)), float(np.quantile(diffs, 0.975))


def add_df_table_docx(doc: Document, df: pd.DataFrame, floatfmt: str = "{:.3f}") -> None:
    table = doc.add_table(rows=1, cols=df.shape[1])
    table.style = "Table Grid"
    hdr = table.rows[0].cells
    for j, col in enumerate(df.columns):
        r = hdr[j].paragraphs[0].add_run(str(col))
        r.bold = True
    for _, r in df.iterrows():
        cells = table.add_row().cells
        for j, col in enumerate(df.columns):
            v = r[col]
            if isinstance(v, (float, np.floating)) and not pd.isna(v):
                cells[j].text = floatfmt.format(float(v))
            else:
                cells[j].text = str(v)


def df_to_rl_table(df: pd.DataFrame, floatfmt: str = "{:.3f}") -> Table:
    data = [list(df.columns)]
    for _, r in df.iterrows():
        row = []
        for col in df.columns:
            v = r[col]
            if isinstance(v, (float, np.floating)) and not pd.isna(v):
                row.append(floatfmt.format(float(v)))
            else:
                row.append(str(v))
        data.append(row)
    tbl = Table(data)
    tbl.setStyle(TableStyle([
        ("BACKGROUND",(0,0),(-1,0), colors.lightgrey),
        ("GRID",(0,0),(-1,-1), 0.25, colors.grey),
        ("FONTNAME",(0,0),(-1,0), "Helvetica-Bold"),
        ("FONTSIZE",(0,0),(-1,-1), 9),
        ("VALIGN",(0,0),(-1,-1), "MIDDLE"),
        ("ALIGN",(1,1),(-1,-1), "CENTER"),
        ("LEFTPADDING",(0,0),(-1,-1), 4),
        ("RIGHTPADDING",(0,0),(-1,-1), 4),
    ]))
    return tbl


